uint8_t shift_active = 0;


void _key_action(uint8_t scancode) {

    //check if a key was released
    if(scancode > 0x80) {
        // check if shift key got released
        if(scancode == 0xaa) shift_active = 0;

        return;
    }

    //else a key was pressed
    //check if shift was pressed
    if(scancode == 0x2a) shift_active = 1;

    char key[2];
    key[0] = key_codes[scancode];
    key[1] = 0;

    if(shift_active == 1) key[0] = shift_map[scancode];

    //if(key_codes[scancode] == 0) key[0] = scancode; //for debugging
    screen_print_at(key, -1, -1);
}


static void keyboard_callback(registers_t *regs) {
    uint8_t scancode = in_byte(0x60);

    _key_action(scancode);
}

void init_keyboard() {
    register_interrupt_handler(IRQ1, keyboard_callback);
}